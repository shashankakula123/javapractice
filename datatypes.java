public class Main {

    public static void main(String[] args) {
        // printing different types of datattypes
	    System.out.println("datatypes");

	    //datatype     declaration


        boolean bool = false;
        System.out.println(bool);
        // size of boolean 1-bit and literals are true,false
        byte bt = 120;
        System.out.println(bt);
        System.out.println(Byte.MIN_VALUE);
        System.out.println(Byte.MAX_VALUE);
        //SIZE OF BYTE VARAIBLE IS 8-BITS AND RANGES FROM -128 TO 127
        char sh ='s';
        System.out.println(sh);
        System.out.println(Character.MIN_VALUE);
        System.out.println(Character.MAX_VALUE);
        //SIZE OF CHAR IS 16-BIT AND CHARACTER REPRESENRTATION OF ASCII VALUES IS 0 TO 255
        short k=24;
        System.out.println(k);
        System.out.println(Short.MIN_VALUE);
        System.out.println(Short.MAX_VALUE);
        //SIZE OF SHORT IS 16-BIT
        int a=578;
        System.out.println(a);
        //SIZE OF INT IS 32-BIT
        System.out.println(Integer.MIN_VALUE);
        System.out.println(Integer.MAX_VALUE);

        long l =78910;
        System.out.println(l);
        System.out.println(Long.MIN_VALUE);
        System.out.println(Long.MAX_VALUE);
        //SIZE OF long IS 64-BIT
        float d= 80;
        System.out.println(d);
        System.out.println(Float.MIN_VALUE);
        System.out.println(Float.MAX_VALUE);
        //SIZE OF FLOAT IS 32-BIT
        double de= 24.565657576;
        System.out.println(de);
        System.out.println(Double.MIN_VALUE);
        System.out.println(Double.MAX_VALUE);
        //SIZE OF CAHR IS 64-BIT
    }
}
